CREATE TABLE MESSAGES (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLE VARCHAR2(9),
    CONTENT CLOB,
    MODEL VARCHAR2(20),
    INSERT_TIMESTAMP TIMESTAMP
);

CREATE TABLE MODELS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(9),
    VERSION VARCHAR2(50),
    URL VARCHAR2(100),
    API_KEY VARCHAR2(200),
    TYPE VARCHAR2(5)
);
commit;
